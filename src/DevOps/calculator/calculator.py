"""Module providing functions about calculator"""

def add_number(a, b):
    """ a plus b
    
    Args:
        a : a number
        b : a number
    
    Returns:
        number is a + b
    """
    return a + b

def sub_number(a, b):
    """ a subtract b
    
    Args:
        a : a number
        b : a number
    
    Returns:
        number is a - b
    """
    return a - b

def div_number(a, b):
    """ a div b
    
    Args:
        a : a number
        b : a number
    
    Returns:
        number is a // b
    """
    return a // b

def mul_number(a, b):
    """ a mul b
    
    Args:
        a : a number
        b : a number
    
    Returns:
        number is a * b
    """
    return a * b
